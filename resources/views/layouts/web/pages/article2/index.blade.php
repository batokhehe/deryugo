                    <p>Desain grafis adalah profesi yang lazim dan banyak ditekuni. Desainer grafis pun tersebar dimana-mana. Mungkin, entah dibelahan bumi bagian mana kamu akan bertemu dengan salah satunya, terjadilah interaksi dan akhirnya.. jatuh cinta! Naah Sebelum semua terlambat, Yakaan… ada baiknya nih kamu membaca artikel ini. Di artikel 6 Alasan Untuk Tidak Kencan Dengan Desainer Grafis terdapat 6 daftar mengapa sebaiknya kamu tidak berkencan (apalagi menikah) dengan Seorang desainer grafis.</p>
                    <h5>1.	Aneh Dan Langka</h5>
                    <p>
                    Desainer grafis itu cenderung aneh baik dalam berfikir maupun bertindak. Mereka lebih suka menganalisa tekstur dan pola di meja cafe daripada mendengar kamu bicara. Mereka lebih ingat kapan Adobe Photoshop terbaru akan launching dari pada ingat kapan kamu ulang tahun.
                    </p>
                    <h5>2.	Punya Definisi Yang Berbeda Soal “Kencan”</h5>
                    <p>
                    Bagi mereka, kencan itu berarti dua hal : “Brainstorming” dan “Client Meeting”. Dari kedengarannya sudah bisa ditebak, kedua-duanya membosankan. Dan pada saat mengobrol, mereka akan menganalisa obrolan berdasarkan layer.
                    </p>
                    <h5>3.	Tidak Punya Waktu</h5>
                    <p>
                    Ehm..maksudnya tidak punya waktu untuk hal selain daripada desain grafis. Mereka akan meninggalkan komputer/laptop cuma karena dua hal : ngantuk dan lapar. Kalaupun mereka mau diajak keluar, mereka akan membawa note book dan pensil untuk doodling. Dan akhirnya kamu akan sadar itu sama saja dengan jalan sendirian.
                    </p>
                    <h5>4.	Hobi Shopping</h5>
                    <p>
                    Tentu bukan shopping sembarang shopping, tapi shopping segala barang yang super aneh. Seperti graphic tablet mungkin, yang suatu saat akan kamu pakai sebagai alas untuk mencincang bawang. Mereka lebih memilih untuk menghabiskan uang membeli font daripada membelikanmu hadiah ulang tahun. Oh God, what a geek!
                    </p>
                    <h5>5.	Tukang Kritik</h5>
                    <p>
                    Desainer grafis itu hobi mengkritik, dan kamu..ehm..akan diajak untuk setuju dan ikut mengkritik. Segala hal berbau visual dikritik, dari mulai menu makanan di kafe, desain di baliho, logo toko-toko dijalan, sampai dengan kombinasi warna pakaian yang kamu pakai. Tidak lupa, kamu juga akan diceramahi tentang “psikologi warna” oleh mereka.
                    </p>
                    <h5>6.	Suka Hal-Hal Yang Sederhana</h5>
                    <p>
                    Salah satu prinsip yang mereka pegang betul adalah simplicity atau kesederhanaan. Itu seperti rukun iman bagi mereka. Segala hal dibuat sederhana : makan ditempat yang sederhana (sekalian biar hemat), nikah juga resepsinya sederhana, sampai dengan ciuman juga harus sederhana.
                    </p>
